# Write a program to accept three integer numbers and find its
# average.


print("please enter the three numbers in order and follow given instructions")

num1 = int(input("please enter the first number = "))
num2 = int(input("please enter the second number = "))
num3 = int(input("please enter the third number = "))

average_Is = (num1 + num2 + num3)//3

print(f"The Average of num1 = {num1} , num2 = {num2} , num3 = {num3} is {average_Is}")
